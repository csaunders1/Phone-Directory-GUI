    public void clear(){
        this.name.setText("");
        this.tel.setText("");
        this.email.setText("");
        this.address.setText("");
        this.dob.setText("");
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        display.setText("");
        list.addToFront(name.getText(), tel.getText(), email.getText(), address.getText(), dob.getText());
        JOptionPane.showMessageDialog(null, "Node has been added!");
        clear();
    }
	
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        list.addToBack(name.getText(), tel.getText(), email.getText(), address.getText(), dob.getText());
        JOptionPane.showMessageDialog(null, "Node has been added!");
        display.setText("");
        clear();
    }
	
